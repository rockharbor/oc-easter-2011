			<h1>Directions</h1>
		</header>
		<p><strong>How do I get to the Pacific Amphitheatre?</strong></p>
		<p>The Pacific Amphitheatre is located in the Southwest corner of the OC Fair and Event Center, near the corner of Fair Drive and Fairview, at 100 Fair Drive, Costa Mesa.</p>
		<p>From Los Angeles: 405 Freeway South, exit at Fairview Road, turn right on Fairview, left on Fair Drive. Or the 5 Freeway South to the 55 Freeway South and exit at Del Mar/Fair Drive, turn right on Fair Drive.</p>
		<p>From Pasadena/San Gabriel Valley: Proceed to the 57 Freeway South (using the 210, 10 or 60 Freeways) to the 5 Freeway South to the 55 Freeway South and exit Del Mar/Fair Drive, turn right on Fair Drive.</p>
		<p>From LAX/Long Beach: 405 Freeway South, exit at Fairview Road, turn right on Fairview, left on Fair Drive.</p>
		<p>From Riverside: 91 Freeway West to 55 Freeway South, exit Del Mar/Fair Drive, turn right on Fair Drive. From San Diego: 5 Freeway North to 55 Freeway South, exit Del Mar/Fair Drive, turn right on Fair Drive.</p>
