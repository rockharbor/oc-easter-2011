			<h1>What to Expect</h1>
		</header>
		<p>Easter at the Pacific Amphitheatre is a celebration of the freedom offered through the death and resurrection of Jesus Christ.</p>
		<p>The story of Easter will be told through a blend of dance, music, and teaching from the Bible. Freedom and new life in Jesus will be celebrated through baptism, where a person is submerged and lifted out of water. This act is both a declarative statement to choose to follow Jesus, as well as a symbolic passage into the new life He offers.</p>
